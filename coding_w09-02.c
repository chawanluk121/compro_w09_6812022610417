#include<stdio.h>

int main(){

int sum,p,x,y;
sum = 0;
for (x = 1,y = 1 ;x * y <= 15 ; x++ ,y += 2)
{
p = x * y ;
sum = sum + p;
printf("%d*%d = %d\n",x,y,p);
}
printf("summation of x * y = %d\n",sum);

return 0;
}
/*
กำหนดตัวแปร sum,p,x,y
กำหนดค่า sum เท่ากับ 0
กำหนดค่า x เท่ากับ 1 ค่า y เท่ากับ 1 ลูปทำงานเมื่อ x * y <= 15 ค่าของ x เพิ่มขึ้นที่ละ 1 ค่าของ y เพิ่มขึ้นที่ละ 2
ผลคูณของ x,y เก็บไว้ใน p
บวกค่า p เข้าไปใน sum
แสดงผลลัพธ์ x * y = p
แสดงผลลัพธ์ของค่าทั้งหมดที่คำนวณได้
จบการทำงาน
*/